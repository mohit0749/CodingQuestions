func nextGreatestLetter(letters []byte, target byte) byte {
	l, h := 0, len(letters)-1
	//ans:=letters[0]
	for l < h {
		var mid int = (l+h)>>1 + 1
		if letters[mid] <= target {
			l = mid + 1
		} else if letters[mid] > target {
			h = mid - 1
		}
	}
	if letters[h] > target {
		return letters[h]
	} else if h+1 > len(letters)-1 {
		return letters[0]
	}
	return letters[h+1]
}
